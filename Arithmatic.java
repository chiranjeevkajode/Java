
// Arithmatic operators in java
public class Arithmatic {
    public static void main(String[] args) {
        int num=20;
        int num1=30;

        System.out.println(num + num1);
        // postfix operators
        num++;
        System.out.println(num);
        num1++;
        ++num;
        System.out.println(num);
        ++num1;
        System.out.println(num1);
        System.out.println(num1);
        // postfix operators
        System.out.println(num++);
        System.out.println(num);
        // prefix operators
        System.out.println(++num);
    }
    
}
