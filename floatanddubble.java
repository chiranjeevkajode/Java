public class floatanddubble {
    public static void main(String[] args) {
        int mint= 22/10;
        float mFloat=22/7f;
        double mDouble=22/7d;
        System.out.println("Int is "+ mint);
        System.out.println("Float is "+ mFloat);
        System.out.println("Double is "+ mDouble);

    }
}
